#Average of 5 grades

Fil=eval(input("Enter your grade in Filipino: "))
Math=eval(input("Enter your grade in Math: "))
Eng=eval(input("Enter your grade in English: "))
Sci=eval(input("Enter your grade in Science: "))
Comp=eval(input("Enter your grade in Computer: "))

average=(Fil+Math+Eng+Sci+Comp)/5

print("The average of your grades is ", average)